/* eslint-disable import/no-extraneous-dependencies */
const gulp = require('gulp')

/* #############################################################################
### Configuration ##############################################################
############################################################################# */

const staticSrc = ['app/images/*', 'app/*.{html,txt,ico}']

/* #############################################################################
### Subtasks ###################################################################
############################################################################# */

gulp.task('watch statics', () =>
    gulp.watch(
        staticSrc,
        ['rewrite html']
    )
)

gulp.task('watch styles', () =>
    gulp.watch(
        ['app/**/*.styl'],
        ['build styles', 'build scripts']
    )
)
